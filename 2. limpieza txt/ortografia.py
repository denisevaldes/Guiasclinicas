# Función para corregir el texto
def corregir_texto(archivo_entrada, archivo_salida):
    # Leer el archivo de entrada
    with open(archivo_entrada, "r", encoding="utf-8") as file:
        texto = file.read()

    # Reemplazar las palabras incorrectas por las correctas sin importar mayúsculas/minúsculas
    for palabra_incorrecta, palabra_correcta in correcciones.items():
        # Usamos re.sub para reemplazar la palabra, ignorando mayúsculas/minúsculas
        texto = re.sub(rf"\b{palabra_incorrecta}\b", palabra_correcta, texto, flags=re.IGNORECASE)

    # Escribir el texto corregido en el archivo de salida
    with open(archivo_salida, "w", encoding="utf-8") as file:
        file.write(texto)

